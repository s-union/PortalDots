func Defined() []Definition {
	return slices.Clone(defined)
}

func Find(name string) (Definition, bool) {
	for _, item := range defined {
		if item.Name == name {
			return item, true
		}
	}
	return Definition{}, false
}

func IsDefined(name string) bool {
	_, ok := Find(name)
	return ok
}

func HasAny(granted []string, candidates ...string) bool {
	for _, candidate := range candidates {
		if slices.Contains(granted, candidate) {
			return true
		}
	}
	return false
}
